# Este sprint enseña cómo listar los contenedores en ejecución y los que ya finalizaron.
# También muestra cómo detener y eliminar contenedores manualmente.

# Comandos a ejecutar en la terminal:
# docker ps           # Lista los contenedores en ejecución
# docker ps -a        # Lista todos los contenedores (incluidos los detenidos)
# docker stop NOMBRE  # Detiene un contenedor por nombre o ID
# docker rm NOMBRE    # Elimina un contenedor detenido
